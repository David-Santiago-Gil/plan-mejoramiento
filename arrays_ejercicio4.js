// Ejercicio 4: Añadir elementos al inicio de un array con unshift()
// Crea un array y le añade elementos al principio.

let numeros = [3, 4, 5];
console.log("Array original:", numeros);

numeros.unshift(2);
console.log("Array después de unshift(2):", numeros);

numeros.unshift(0, 1);
console.log("Array después de unshift(0, 1):", numeros);

alert("Se ha demostrado el uso de unshift() en la consola.");
