// Crea un array y elimina el primer elemento.

let planetas = ["Mercurio", "Venus", "Tierra", "Marte"];
console.log("Array original:", planetas);

let planetaEliminado = planetas.shift();
console.log("Elemento eliminado con shift():", planetaEliminado);
