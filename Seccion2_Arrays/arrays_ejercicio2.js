// Ejercicio 2: Eliminar elementos de un array con pop()
// Crea un array y elimina el último elemento.

let paises = ["Alemania", "Brasil", "China", "Dinamarca"];
console.log("Array original:", paises);

let paisEliminado = paises.pop();
console.log("Elemento eliminado con pop():", paisEliminado);
console.log("Array después de pop():", paises);

alert("Se ha demostrado el uso de pop() en la consola.");
