// Convierte un número a una cadena de texto.

let numeroOriginal = 100;
let textoConvertido = String(numeroOriginal);

console.log("El tipo de dato original es: " + typeof numeroOriginal);
console.log("El valor original es: " + numeroOriginal);
console.log("--------------------");
console.log("El tipo de dato después de la conversión es: " + typeof textoConvertido);
console.log("El valor convertido es: '" + textoConvertido + "'");

alert("Se ha realizado una conversión de número a string en la consola.");
