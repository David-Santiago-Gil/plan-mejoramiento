// Ejercicio 9: Demostración de 'let'
// Muestra cómo una variable declarada con 'let' puede ser reasignada.

let contador = 10;
console.log("El valor inicial del contador es: " + contador);

contador = 20;
console.log("El valor del contador después de la reasignación es: " + contador);

alert("Se ha demostrado el uso de 'let' en la consola.");
