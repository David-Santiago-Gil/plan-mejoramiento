// Ejercicio 1: Añadir elementos a un array con push()
// Crea un array y le añade elementos al final.

let frutas = ["manzana", "banana", "cereza"];
console.log("Array original:", frutas);

frutas.push("dátil");
console.log("Array después de push('dátil'):", frutas);

frutas.push("elote", "frambuesa");
console.log("Array después de push('elote', 'frambuesa'):", frutas);

alert("Se ha demostrado el uso de push() en la consola.");
